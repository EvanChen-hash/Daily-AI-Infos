const state = {
  category: "All",
  source: "All",
  query: "",
  activeDate: ""
};

const visibleCategories = ["Official", "Tweets"];
const categoryOrder = ["All", ...visibleCategories];
const dashboardTimeZone = "Asia/Tokyo";
const todayKey = new Intl.DateTimeFormat("en-CA", {
  timeZone: dashboardTimeZone,
  year: "numeric",
  month: "2-digit",
  day: "2-digit"
}).format(new Date(feedData.generatedAt));

const els = {
  dateList: document.querySelector("#dateList"),
  searchInput: document.querySelector("#searchInput"),
  resetButton: document.querySelector("#resetButton"),
  categoryFilters: document.querySelector("#categoryFilters"),
  sourceFilters: document.querySelector("#sourceFilters"),
  themeToggle: document.querySelector("#themeToggle"),
  statsGrid: document.querySelector("#statsGrid"),
  feed: document.querySelector("#feed")
};

const themeStorageKey = "daily-ai-info-theme";

function formatDate(value) {
  return value.replace(/-/g, "/");
}

function getFilteredItems() {
  const query = state.query.trim().toLowerCase();
  return feedData.items
    .filter((item) => visibleCategories.includes(item.category))
    .filter((item) => state.category === "All" || item.category === state.category)
    .filter((item) => state.source === "All" || item.source === state.source)
    .filter((item) => {
      if (!query) return true;
      return [item.title, item.summary, item.source, item.category, item.tags.join(" ")]
        .join(" ")
        .toLowerCase()
        .includes(query);
    })
    .sort((a, b) => b.date.localeCompare(a.date));
}

function groupedByDate(items) {
  return items.reduce((groups, item) => {
    groups[item.date] ||= [];
    groups[item.date].push(item);
    return groups;
  }, {});
}

function getInitialTheme() {
  const storedTheme = localStorage.getItem(themeStorageKey);
  if (storedTheme === "dark" || storedTheme === "light") return storedTheme;
  return window.matchMedia?.("(prefers-color-scheme: dark)").matches ? "dark" : "light";
}

function applyTheme(theme) {
  document.documentElement.dataset.theme = theme;
  localStorage.setItem(themeStorageKey, theme);
  if (!els.themeToggle) return;
  const isDark = theme === "dark";
  els.themeToggle.setAttribute("aria-pressed", String(isDark));
  els.themeToggle.setAttribute("aria-label", `Switch to ${isDark ? "light" : "dark"} mode`);
  const label = els.themeToggle.querySelector(".theme-toggle-label");
  if (label) label.textContent = isDark ? "Dark" : "Light";
}

function getCategoryCounts() {
  const visibleItems = feedData.items.filter((item) => visibleCategories.includes(item.category));
  const counts = { All: visibleItems.length };
  for (const cat of visibleCategories) {
    counts[cat] = visibleItems.filter((item) => item.category === cat).length;
  }
  return counts;
}

function renderStats() {
  if (!els.statsGrid) return;
  const visibleItems = feedData.items.filter((item) => visibleCategories.includes(item.category));
  const totals = [
    { label: "Total items", value: visibleItems.length },
    { label: "Official", value: visibleItems.filter((i) => i.category === "Official").length },
    { label: "X posts", value: visibleItems.filter((i) => i.category === "Tweets").length },
    { label: "Sources", value: new Set(visibleItems.map((i) => i.source)).size }
  ];
  els.statsGrid.innerHTML = totals
    .map(({ label, value }) => `
      <div class="stat-card">
        <span class="stat-label">${label}</span>
        <span class="stat-value">${value}</span>
      </div>
    `)
    .join("");
}

function renderCategoryFilters() {
  const counts = getCategoryCounts();
  els.categoryFilters.innerHTML = categoryOrder
    .map((category) => {
      const count = counts[category] ?? 0;
      return `<button class="filter-chip ${state.category === category ? "active" : ""}" data-category="${category}" type="button">${category}<span class="chip-count">${count}</span></button>`;
    })
    .join("");

  els.categoryFilters.querySelectorAll("button").forEach((button) => {
    button.addEventListener("click", () => {
      state.category = button.dataset.category;
      render();
    });
  });
}

function renderSourceFilters() {
  const visibleItems = feedData.items.filter((item) => visibleCategories.includes(item.category));
  const sources = ["All", ...new Set(visibleItems.map((item) => item.source))];
  els.sourceFilters.innerHTML = sources
    .map((source) => `<button class="filter-chip ${state.source === source ? "active" : ""}" data-source="${source}" type="button">${source}</button>`)
    .join("");

  els.sourceFilters.querySelectorAll("button").forEach((button) => {
    button.addEventListener("click", () => {
      state.source = button.dataset.source;
      render();
    });
  });
}

function renderDates() {
  const visibleItems = getFilteredItems();
  const dates = [...new Set(visibleItems.map((item) => item.date).sort().reverse())];
  if (!state.activeDate || !dates.includes(state.activeDate)) {
    state.activeDate = dates[0] || "";
  }
  const countByDate = visibleItems.reduce((acc, item) => {
    acc[item.date] = (acc[item.date] || 0) + 1;
    return acc;
  }, {});
  els.dateList.innerHTML = dates
    .map((date) => {
      const label = date === todayKey ? "Today" : formatDate(date).slice(5);
      const count = countByDate[date] || 0;
      return `<button class="date-pill ${state.activeDate === date ? "active" : ""}" data-date="${date}" type="button"><span>${label}</span><span class="date-pill-count">${count}</span></button>`;
    })
    .join("");

  els.dateList.querySelectorAll("button").forEach((button) => {
    button.addEventListener("click", () => {
      state.activeDate = button.dataset.date;
      syncActiveDate();
      document.querySelector(`[data-section-date="${state.activeDate}"]`)?.scrollIntoView({
        behavior: "smooth",
        block: "start"
      });
    });
  });
}

function renderFeed(items) {
  if (!items.length) {
    els.feed.innerHTML = `<div class="empty">No items match the current filters.</div>`;
    return;
  }

  const groups = groupedByDate(items);
  els.feed.innerHTML = Object.entries(groups)
    .map(([date, groupItems]) => `
      <section class="date-section" data-section-date="${date}">
        <div class="date-section-heading">
          <span>${date === todayKey ? "Today" : formatDate(date).slice(5)}</span>
          <i aria-hidden="true"></i>
        </div>
        <div class="event-grid">${groupItems.map(renderCard).join("")}</div>
      </section>
    `)
    .join("");
}

function renderCard(item) {
  const isTweet = item.category === "Tweets";
  const resourceLabel = isTweet ? "X post" : "Official";
  const resourceIcon = isTweet ? "X" : "O";
  return `
    <article class="feed-card" data-id="${item.id}" data-category="${item.category}">
      <div class="card-topline">
        <span class="resource-tag ${isTweet ? "tweet-pill" : "official-pill"}"><span class="tag-icon" aria-hidden="true">${resourceIcon}</span>${resourceLabel}</span>
        <a class="source-link" href="${item.url}" target="_blank" rel="noreferrer">${item.source}</a>
      </div>
      <h4>${item.title}</h4>
      <p class="card-summary">${item.summary}</p>
      <p class="card-impact"><strong>Why it matters:</strong> ${item.impact}</p>
      <div class="tags">${item.tags.slice(0, 5).map((tag) => `<span class="tag ${isTweet ? "tweet-tag" : "official-tag"}"><span class="tag-dot" aria-hidden="true"></span>${tag}</span>`).join("")}</div>
    </article>
  `;
}

function syncActiveDate() {
  els.dateList.querySelectorAll(".date-pill").forEach((node) => {
    node.classList.toggle("active", node.dataset.date === state.activeDate);
  });
}

function updateActiveDateFromScroll() {
  const sections = [...document.querySelectorAll(".date-section")];
  const current = sections
    .map((section) => ({
      date: section.dataset.sectionDate,
      distance: Math.abs(section.getBoundingClientRect().top - 180)
    }))
    .sort((a, b) => a.distance - b.distance)[0];
  if (current && current.date !== state.activeDate) {
    state.activeDate = current.date;
    syncActiveDate();
  }
}

function render() {
  const items = getFilteredItems();
  renderStats();
  renderCategoryFilters();
  renderSourceFilters();
  renderDates();
  renderFeed(items);
  requestAnimationFrame(updateActiveDateFromScroll);
}

els.searchInput.addEventListener("input", (event) => {
  state.query = event.target.value;
  render();
});

els.resetButton.addEventListener("click", () => {
  state.category = "All";
  state.source = "All";
  state.query = "";
  state.activeDate = "";
  els.searchInput.value = "";
  render();
});

els.themeToggle?.addEventListener("click", () => {
  const currentTheme = document.documentElement.dataset.theme === "dark" ? "dark" : "light";
  applyTheme(currentTheme === "dark" ? "light" : "dark");
});

applyTheme(getInitialTheme());
render();

window.addEventListener("scroll", updateActiveDateFromScroll, { passive: true });
